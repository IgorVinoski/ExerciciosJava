package Exercicios_estruturas_de_repeticao;

public class Exercicio7 {
    public static void main(String[] args){
        /*Faça um programa que apresente os quadrados dos números inteiros de
15 a 200.*/
        int i = 15;
        while(i<200){
            System.out.println(i*i);
            i++;
        }
    }
}
